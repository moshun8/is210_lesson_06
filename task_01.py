"""Evens and Odds"""


def evens_and_odds(numbers=[], show_even=True):
    evens = []
    odds = []
    for num in numbers:
        if num % 2 == 0:
             num is "even"
        else:
             num is "odd"
             odds.append(num)
    while show_even:
        if num is "even":
             evens.append(num)
        else:
             odds.append(num)